// ---------------------------------------------------------------------------
// principal_object — shared base class for Person, Property, Document
// ---------------------------------------------------------------------------
//
// Every Person, Property and Document row has exactly one corresponding
// principal_object row. The code (e.g. PERS00001, PROP00002) lives here and
// is generated from a single shared Postgres sequence
// (`principal_object_code_seq`), so numbers are unique across all three types.
//
// The sequence is created in the migration (not expressible in Drizzle).
// The code is NOT auto-generated by a DEFAULT — the application layer inserts
// this row first (supplying code via a sql`` nextval expression), then inserts
// the domain row with the returned id and code value.
//
// NOTE (Slice #15.05): the enum value was renamed from "PAPERWORK" to
// "DOCUMENT" — existing rows are migrated by migration_020_rename_to_document.sql.

export const principalObjectTypeEnum = pgEnum("principal_object_type", [
  "PERSON",
  "PROPERTY",
  "DOCUMENT",
]);

export const principalObject = pgTable("principal_object", {
  id: uuid("id").primaryKey().defaultRandom(),
  // Full code including prefix, e.g. "PERS00003". Supplied by app layer.
  code: text("code").notNull().unique(),
  objectType: principalObjectTypeEnum("object_type").notNull(),
  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
});

// NOTE: `integer` is used below for sort_order on the lookup tables.

// ---------------------------------------------------------------------------
// Enums
// ---------------------------------------------------------------------------

export const personTypeEnum = pgEnum("person_type", ["NATURAL", "JUDICIAL"]);

export const addressKindEnum = pgEnum("address_kind", [
  "HOME",
  "POSTAL",
  "HEADQUARTERS",
  "CORRESPONDENCE",
]);

export const idDocumentTypeEnum = pgEnum("id_document_type", [
  "ID_CARD",
  "PASSPORT",
]);

export const genderEnum = pgEnum("gender", ["MALE", "FEMALE"]);

// ---------------------------------------------------------------------------
// person — base / supertype
// ---------------------------------------------------------------------------
//
// Holds everything true of any person (natural or judicial). All M:M junction
// tables FK to `person.id` and never need to know the subtype.

export const person = pgTable("person", {
  id: uuid("id").primaryKey().defaultRandom(),

  // FK to the shared principal_object row that owns this person's code.
  // Set at creation; never changes.
  principalObjectId: uuid("principal_object_id")
    .notNull()
    .unique()
    .references(() => principalObject.id),

  // Human-readable code like "PERS00001". Copied from principal_object.code
  // at creation time. No DEFAULT — supplied by the app layer (see queries.ts).
  code: text("code").notNull().unique(),

  type: personTypeEnum("type").notNull(),

  // Cached display name — populated by API code on insert/update from the
  // subtype (first_name + last_name for natural; company name for judicial
  // when that lands in Slice #1.5). Used by the list view and search.
  displayName: text("display_name").notNull(),

  notes: text("notes"),

  // Email of the user who last wrote this record (set by the API layer from
  // the Supabase session; null for legacy rows and seed data).
  updatedBy: text("updated_by"),

  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
  updatedAt: timestamp("updated_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
  deletedAt: timestamp("deleted_at", { withTimezone: true }),
});

// ---------------------------------------------------------------------------
// natural_person — subtype for humans
// ---------------------------------------------------------------------------
//
// 1:1 with `person`. PK is also FK to person.id with ON DELETE CASCADE so
// hard-deleting a person also removes the satellite row. Soft delete (the
// API path) just sets person.deleted_at and leaves this row untouched.

export const naturalPerson = pgTable(
  "natural_person",
  {
    personId: uuid("person_id")
      .primaryKey()
      .references(() => person.id, { onDelete: "cascade" }),

    firstName: text("first_name"),
    lastName: text("last_name"),
    nickname: text("nickname"),

    // CNP — Romanian Personal Numeric Code. Optional (foreign nationals
    // may not have one). UNIQUE when present, but only among non-soft-deleted
    // persons — enforced by a trigger (migration_025), not a plain unique
    // index, since the partial index has no way to see person.deleted_at
    // (soft-delete lives on the parent `person` row). Once set, cannot be
    // changed — enforced by a separate trigger (NULL -> value is allowed;
    // value -> anything-different is not).
    cnp: text("cnp"),

    idDocumentType: idDocumentTypeEnum("id_document_type"),
    idDocumentNumber: text("id_document_number"),

    gender: genderEnum("gender"),
    // `mode: "string"` keeps DOB as ISO date strings ("1985-02-03") on both
    // read and write — avoids the Date-object timezone gotcha for a calendar
    // date with no time component.
    dateOfBirth: date("date_of_birth", { mode: "string" }),

    personalPhone1: text("personal_phone_1"),
    personalPhone2: text("personal_phone_2"),
    workPhone: text("work_phone"),
    personalEmail1: text("personal_email_1"),
    personalEmail2: text("personal_email_2"),
    workEmail: text("work_email"),

    // Added in Slice 14.15.01 — fields read off a Romanian ID card that have
    // no existing home, plus a citizenship FK. All nullable; populated either
    // manually or via the Import/Classify "Person" flow's vision extraction.
    placeOfBirth: text("place_of_birth"),
    idIssuingAuthority: text("id_issuing_authority"),
    idValidFrom: date("id_valid_from", { mode: "string" }),
    idValidUntil: date("id_valid_until", { mode: "string" }),
    idCardNumber: text("id_card_number"),
    idMrzRaw: text("id_mrz_raw"),
    citizenshipId: uuid("citizenship_id").references(() => lookupCitizenship.id, { onDelete: "set null" }),

    // "Tip Profesional" — Physical Person Type, admin-managed via
    // Administration → Reference Data → "Physical Person Types".
    // Nullable; ON DELETE SET NULL so removing a type just clears the tag.
    // Added in Slice #18.16.VL.
    physicalPersonTypeId: uuid("physical_person_type_id")
      .references(() => lookupPersonType.id, { onDelete: "set null" }),

    // When true the UI hides the CORRESPONDENCE address block and no
    // CORRESPONDENCE address row is stored — mirrors correspondenceSameAsHq
    // on judicial_person. Added in Slice #19.01.
    correspondenceSameAsHome: boolean("correspondence_same_as_home")
      .notNull()
      .default(false),
  },
  (t) => [
    // At least one of first_name / last_name must be set.
    check(
      "natural_person_has_name",
      sql`${t.firstName} IS NOT NULL OR ${t.lastName} IS NOT NULL`,
    ),
    // NOTE: CNP uniqueness is NOT enforced here as a plain unique index.
    // A plain partial unique index can't see person.deleted_at (soft-delete
    // lives on the parent `person` row, not this table), so it would
    // permanently block reusing a CNP after its person was soft-deleted.
    // Enforced instead by a BEFORE INSERT OR UPDATE trigger
    // (natural_person_check_cnp_unique, migration_025) that only counts
    // collisions against non-soft-deleted persons.
  ],
);

// ---------------------------------------------------------------------------
// judicial_person — subtype for organisations / legal entities
// ---------------------------------------------------------------------------
//
// 1:1 with `person`. PK is also FK to person.id with ON DELETE CASCADE so
// hard-deleting a person also removes the satellite row. Soft delete (the
// API path) just sets person.deleted_at and leaves this row untouched.
//
// `notes` lives on the parent `person` row (same as natural_person).
// HEADQUARTERS + CORRESPONDENCE addresses are stored in the existing
// `address` table; both kinds already exist in addressKindEnum.

export const judicialPerson = pgTable(
  "judicial_person",
  {
    personId: uuid("person_id")
      .primaryKey()
      .references(() => person.id, { onDelete: "cascade" }),

    // Display / legal name. Required.
    name: text("name").notNull(),
    nickname: text("nickname"),

    // Judicial-person legal/organisational form (SRL/SA/PFA/etc.). Was a
    // fixed Postgres enum until Slice #15.07 — now an FK to the admin-managed
    // lookup_judicial_person_type table (Administration -> Reference Data ->
    // "Judicial Person Types"). Nullable; ON DELETE SET NULL so removing a
    // type from Reference Data just clears the tag rather than blocking the
    // delete or cascading.
    judicialPersonTypeId: uuid("judicial_person_type_id")
      .references(() => lookupJudicialPersonType.id, { onDelete: "set null" }),

    // CUI — Cod Unic de Inregistrare (Romanian fiscal registration code).
    // Optional. UNIQUE when present, but only among non-soft-deleted persons
    // — enforced by a trigger (migration_025), not a plain unique index (see
    // the matching note on natural_person.cnp above for why). Once set,
    // cannot be changed — enforced by a separate trigger (NULL -> value is
    // allowed; value -> anything-different is not).
    cuiNumber: text("cui_number"),

    // Trade Register number — "Nr. ORC" (e.g. "J22/123/2020").
    tradeRegisterNumber: text("trade_register_number"),

    // Contact persons — FK references to natural_person rows.
    // Nullable; ON DELETE SET NULL so removing the linked person just clears
    // the reference without touching this judicial_person row.
    contactPerson1Id: uuid("contact_person_1_id")
      .references(() => person.id, { onDelete: "set null" }),
    contactPerson2Id: uuid("contact_person_2_id")
      .references(() => person.id, { onDelete: "set null" }),

    // When true, the correspondence address mirrors the registered-office
    // address and no CORRESPONDENCE address row is stored in `address`.
    correspondenceSameAsHq: boolean("correspondence_same_as_hq")
      .notNull()
      .default(false),
  },
  () => [
    // NOTE: CUI uniqueness is enforced by a trigger
    // (judicial_person_check_cui_unique, migration_025), not a plain unique
    // index — see the matching note on natural_person.cnp for why.
  ],
);

// ---------------------------------------------------------------------------
// address — owned by a person, one row per (person, kind)
// ---------------------------------------------------------------------------
//
// FK cascades on hard-delete of person. When a user clears an address block
// in the form, we hard-delete the row directly (no deleted_at on address —
// addresses are part of the person's aggregate, not separately archived).

export const address = pgTable(
  "address",
  {
    id: uuid("id").primaryKey().defaultRandom(),

    personId: uuid("person_id")
      .notNull()
      .references(() => person.id, { onDelete: "cascade" }),

    kind: addressKindEnum("kind").notNull(),

    // "Street, No., Block, Floor, Apt" — single free-form field per spec.
    streetLine: text("street_line"),
    postalCode: text("postal_code"),
    // "City, Locality, UAT, Commune, Village" — single free-form field.
    locality: text("locality"),
    // "County / Sector" — free text for now; will become FK later.
    county: text("county"),
    country: text("country").notNull(),
    notes: text("notes"),

    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [
    // A person can have at most one address of each kind.
    uniqueIndex("address_person_kind_unique").on(t.personId, t.kind),
  ],
);

// ---------------------------------------------------------------------------
// person_version — full-snapshot version history  (Slice #18.05)
// ---------------------------------------------------------------------------
//
// One row per saved version of a person (natural OR judicial — a single shared
// table, since both FK person.id). version_number is 0-based: version 0 is the
// state at creation, every saved edit appends the next number. Each row stores
// a COMPLETE snapshot (the person's own editable fields + its address blocks)
// as JSONB — reconstructing "version N" is a direct lookup, no delta replay.
//
// The label colour and per-field highlights shown in the UI are derived at
// display time by diffing snapshot N against snapshot N-1; nothing derived is
// persisted here. The snapshot JSON shape differs by person.type and lives next
// to the write paths in src/lib/persons/queries.ts (NaturalPersonSnapshot) and
// src/lib/judicial-persons/queries.ts (JudicialPersonSnapshot); both match the
// backfill in migration_030_person_versions.sql exactly.

export const personVersion = pgTable(
  "person_version",
  {
    id: uuid("id").primaryKey().defaultRandom(),

    personId: uuid("person_id")
      .notNull()
      .references(() => person.id, { onDelete: "cascade" }),

    // 0-based; unique per person.
    versionNumber: integer("version_number").notNull(),

    // Full snapshot of the person at this version (own fields + addresses).
    // Untyped here to avoid a circular import; the query layer casts it to the
    // subtype's snapshot type.
    snapshot: jsonb("snapshot").notNull(),

    // Email of the user who created this version (from the Supabase session).
    updatedBy: text("updated_by"),

    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [
    uniqueIndex("person_version_person_number_unique").on(
      t.personId,
      t.versionNumber,
    ),
  ],
);

