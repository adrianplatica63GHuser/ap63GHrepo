// ---------------------------------------------------------------------------
// property — root entity
// ---------------------------------------------------------------------------
//
// Soft-delete via deleted_at (same pattern as person).
// Code is auto-generated from a Postgres sequence: PROP00001, PROP00002, ...
// The sequence is created in the migration (not expressible in Drizzle).

export const property = pgTable("property", {
  id: uuid("id").primaryKey().defaultRandom(),

  // FK to the shared principal_object row that owns this property's code.
  principalObjectId: uuid("principal_object_id")
    .notNull()
    .unique()
    .references(() => principalObject.id),

  // Human-readable code like "PROP00002". Copied from principal_object.code
  // at creation time. No DEFAULT — supplied by the app layer.
  code: text("code").notNull().unique(),

  // Property type — admin-managed (lookup_property_type, Reference Data ->
  // Property Types). Was a fixed `property_type` Postgres enum ("LAND") until
  // Slice #15.16. Nullable; ON DELETE SET NULL so removing a type from
  // Reference Data just clears the tag rather than blocking the delete.
  propertyTypeId: uuid("property_type_id")
    .references(() => lookupPropertyType.id, { onDelete: "set null" }),

  // "Porecla / elemente definitorii" — short identifying label.
  nickname: text("nickname"),

  // Romanian cadastral identifiers.
  tarlaSola:       text("tarla_sola"),       // Nr. tarla / sola
  parcela:         text("parcela"),           // Nr. parcela
  cadastralNumber: text("cadastral_number"),  // Nr. cadastru
  carteFunciara:   text("carte_funciara"),    // Nr. carte funciara

  // Use category — admin-managed (lookup_use_category, Reference Data -> Use
  // Categories). Was a fixed `use_category` Postgres enum (CATEG1/2/3) until
  // Slice #15.16. Nullable; ON DELETE SET NULL.
  useCategoryId: uuid("use_category_id")
    .references(() => lookupUseCategory.id, { onDelete: "set null" }),

  // "Suprafata in mp" — official/declared area in square metres (user-entered).
  surfaceAreaMp: numeric("surface_area_mp", { precision: 12, scale: 2 }),

  // Slice #18.09: system-computed area (m²) of the polygon formed by the
  // ordered corners. NOT user-editable — recomputed on every save from the
  // corners (WGS84 → Stereo 70 → shoelace). NULL when fewer than 3 corners.
  calculatedAreaMp: numeric("calculated_area_mp", { precision: 12, scale: 2 }),

  notes: text("notes"),

  // Email of the user who last wrote this record (set by the API layer from
  // the Supabase session; null for legacy rows and seed data).
  updatedBy: text("updated_by"),

  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
  updatedAt: timestamp("updated_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
  deletedAt: timestamp("deleted_at", { withTimezone: true }),
});

// ---------------------------------------------------------------------------
// property_address — single address block per property
// ---------------------------------------------------------------------------
