// ---------------------------------------------------------------------------
// property_corner — ordered polygon vertices, stored as WGS84 decimal degrees
// ---------------------------------------------------------------------------
//
// Corners are the authoritative geometry. The polygon for map display is
// derived from the ordered corner set at query time. Heights are omitted —
// we store 2-D planimetric coordinates only.

export const propertyCorner = pgTable(
  "property_corner",
  {
    id: uuid("id").primaryKey().defaultRandom(),

    propertyId: uuid("property_id")
      .notNull()
      .references(() => property.id, { onDelete: "cascade" }),

    // 1-based display order; unique per property.
    sequenceNo: integer("sequence_no").notNull(),

    // WGS84 / ETRS89 decimal degrees.
    lat: doublePrecision("lat").notNull(),
    lon: doublePrecision("lon").notNull(),

    // Original index token (column 1) from the imported cadastral text/OCR
    // source, kept permanently bound to this corner's lat/lon so it travels
    // with the corner when the table is reordered (e.g. fixing a bow-tie
    // polygon via the up/down arrows). Null when there was no source index
    // (manual entry, OCR groups, or the legacy 2-column text format).
    originalIndex: integer("original_index"),

    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [
    uniqueIndex("property_corner_property_seq_unique").on(
      t.propertyId,
      t.sequenceNo,
    ),
  ],
);

